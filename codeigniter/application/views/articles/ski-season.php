                    <div class="expandable-text"><p>Being Canadian, I got the Youth Mobility Visa for the UK which has a duration of 2 years. It costed £100 and took about a month to get, although I went about it in a very round-about way. At the time of realising that the job hunt was serious and we were going to make it happen, I had been working in Barcelona with a Spanish visa and already booked a flight to Poland to begin a tour of eastern europe. So I ended up being a Canadian with a Spanish residency applying for an English visa in Warsaw but getting it sent to Budapest (where I planned to be around the time it would be sent). I still don’t fully understand the whole process, and if my Spanish residency affected my ability to apply for the visa outside of the UK, but point is to avoid finding yourself in a similar situation, and if you find yourself in the exact situation then you can rest assured that it is possible.</p> 
                        <p>Showing my visa to my employer was all I needed to be employed, placed in payroll and have everything set on the english side. Living in France, inside of the Schengen zone without a Schengen visa was a different story. There is another level of documentation that has to be done by you/your employer to post you in France. This is something that is best to talk to your employer about and confirm that everything is done correctly. </p>
