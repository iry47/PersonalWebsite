                    <div class="expandable-text"><p>I was hired by Barrelli Ski, who operates two large chalets with a capacity of ~15 people each and two smaller apartments with a capacity of ~5. Our team of six was divided in to 2 managers, 2 head chefs and 2 assistants who worked together to provide breakfast, afternoon tea, dinner and cleaning 6 days a week (same meals every week). One of the six days was changeover day, spent almost entirely deep cleaning all the chalets and the seventh day (Wednesday) was completely off. We were able to ski everyday (minus changeover) from about 11-4, which is an average time that was shorter in the beginning of the season and longer after we ironed out the kinks of the job. The accommodation for the staff were private rooms for each couple — the owner realised after several years that couples in the close-knit environment last longer and were generally happier throughout the season. All the staff also received a ski pass for the season, rental equipment, food, and a minimum salary of £400 / month which accumulates slowly but surely when you have no expenses.</p>
