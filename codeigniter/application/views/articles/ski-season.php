                    <div class="expandable-text">Champagny en Vanoise is a small ski resort on the south side of the mountain, with a view across the valley to Les Trois Vallées (super fancy). Luckily, even though Champagny has only two chairlifts and a handful of runs, it connects to La Plagne on the north side of the mountain and Les Arcs across a valley in the north to make the combined terrain of Paradiski. We had access to 141 chairlifts, 967 accomodation options, 475 runs to make up 425km of downhill skiing, 12 ski villages, 5 ski resorts, …I think you get the picture. I really loved the ability to not only explore all the terrain during the day, but also come back to a smaller, authentic French resort that wasn’t filled with vacationers looking to party every single night. Champagny has 1 bar, 1 bar/restaurant, 6 hotels, 1 gondola…the opposite picture. We were easily able  to go out for a quiet beer after a long day of work and ski but also had awesome reggae events to look forward to every couple weeks in Code Bar. We often hear life is about balance and that’s what we found in Champagny, which helps explain why the same people seem to return every year.</div>
